const sleep = ms => new Promise(r => setTimeout(r, ms))

async function generateAndSave({ id, prompt }) {
  const filename = `${id}.png`
  const filepath = join(OUTPUT_DIR, filename)

  if (existsSync(filepath)) {
    console.log(`[skip] ${id} — 已存在`)
    return true
  }

  try {
    console.log(`[生成] ${id}...`)
    const response = await openai.images.generate({
      model: 'dall-e-3',
      prompt,
      n: 1,
      size: '1024x1024',
      quality: 'standard',
      response_format: 'url',
    })

    const url = response.data[0]?.url
    if (!url) throw new Error('未取得圖片 URL')

    const res = await fetch(url)
    if (!res.ok) throw new Error(`下載失敗: ${res.status}`)
    const buffer = Buffer.from(await res.arrayBuffer())
    await writeFile(filepath, buffer)

    console.log(`[完成] ${id} → intro/${filename}`)
    return true
  } catch (err) {
    console.error(`[錯誤] ${id}: ${err.message}`)
    return false
  }
}

async function main() {
  if (!process.env.OPENAI_API_KEY) {
    console.error('錯誤：未設定 OPENAI_API_KEY')
    process.exit(1)
  }

  await mkdir(OUTPUT_DIR, { recursive: true })
  console.log(`開始生成 ${IMAGES.length} 張介紹頁圖片...\n`)

  let done = 0, failed = 0
  for (let i = 0; i < IMAGES.length; i++) {
    const ok = await generateAndSave(IMAGES[i])
    if (ok) done++; else failed++
    if (i < IMAGES.length - 1) await sleep(2500)
  }

  console.log(`\n=== 完成 ===`)
  console.log(`成功：${done}  失敗：${failed}  共：${IMAGES.length}`)
}

main().catch(console.error)
